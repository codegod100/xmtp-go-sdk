//! Generate C header file from the FFI definitions

fn main() {
    cbindgen::Builder::new()
        .with_crate(".")
        .with_language(cbindgen::Language::C)
        .with_include_guard("XMTP_FFI_H")
        .with_include_guard_header(None)
        .with_autogen_warning("/* DO NOT MODIFY: This file is auto-generated by cbindgen */")
        .with_documentation(true)
        .with_no_includes()
        .generate()
        .expect("Unable to generate bindings")
        .write_to_file("xmtp_ffi.h");
    
    println!("Generated xmtp_ffi.h");
}
